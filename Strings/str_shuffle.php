<?php

/**
 * Definition and Usage:
 *    The str_shuffle() function randomly shuffles 
 * all the characters of a string.
 *
 *
 * Syntax:
 *    str_shuffle(string)
 *
 *
 * Parameter Values:
 *    string   => Required. Specifies the string to shuffle
 *
 */

// Example 1:
$string = 'Hello World';
echo str_shuffle($string);
# Output => oHlol delWr (might be differentiate)
